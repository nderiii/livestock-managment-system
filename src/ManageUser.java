    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tableUser.getModel();

        //delete row
        if (tableUser.getSelectedRowCount() == 1) {
            tblmodel.removeRow(tableUser.getSelectedRow());
            // if single row is selected then delete
        } else {
            if (tableUser.getRowCount() == 0) //if table is empty (no data)then display message
            {
                JOptionPane.showMessageDialog(this, "Table is empty");
            } else {
                //if table row is not empty but row is not selected or multiple row is selected
                JOptionPane.showMessageDialog(this, "Please select single row for delete");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

