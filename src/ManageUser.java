    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
    DefaultTableModel tblModel = (DefaultTableModel) tableUser.getModel();
    try
    {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("select *from appuser where userRole='Admin'");
        while(rs.next())
        {
            tblModel.addRow(new Object[]{rs.getString("appuser_pk"),rs.getString("name"),rs.getString("mobilenumber"),rs.getString("email"),rs.getString("address"),rs.getString("status")});
        }
    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(null,e);
    }
    btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String mobilenumber = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String password = txtPassword.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();
        String userRole = "User";

// Check if fields are valid
        if (validateFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pstmt = con.prepareStatement("INSERT INTO appuser ( userRole, name, mobilenumber, email, address, password, status) VALUES (?, ?, ?, ?, ?, ?,?)");
                pstmt.setString(1, userRole);
                pstmt.setString(2, name);
                pstmt.setString(3, mobilenumber);
                pstmt.setString(4, email);
                pstmt.setString(5, address);
                pstmt.setString(6, password);
                pstmt.setString(7, status);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "User added successfully");
                setVisible(false);
                new ManageUser().setVisible(true);
            } catch (SQLException e) {
                e.printStackTrace();  // Log the SQL exception for debugging purposes
                JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
            } catch (Exception e) {
                e.printStackTrace();  // Log any other exceptions for debugging purposes
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }


    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        // TODO add your handling code here:
        int index = tableUser.getSelectedRow();
        TableModel model = tableUser.getModel();

        String id = model.getValueAt(index, 0).toString();
        appuser_pk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtName.setText(name);

        String mobilenumber = model.getValueAt(index, 2).toString();
        txtMobileNumber.setText(mobilenumber);

        String email = model.getValueAt(index, 3).toString();
        txtEmail.setText(email);

        String address = model.getValueAt(index, 4).toString();
        txtAddress.setText(address);

        String status = model.getValueAt(index, 5).toString();
        ComboBoxStatus.removeAllItems();
        if (status.equals("Active")) {
            ComboBoxStatus.addItem("Active");
            ComboBoxStatus.addItem("Inactive");
        } else {
            ComboBoxStatus.addItem("Active");
            ComboBoxStatus.addItem("Inactive");
        }

        txtPassword.setEditable(false);
        txtPassword.setBackground(Color.DARK_GRAY);

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableUserMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String mobilenumber = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String status = (String) ComboBoxStatus.getSelectedItem();

// Check if fields are valid
        if (validateFields("edit")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement pstmt = con.prepareStatement("update appuser set name=?,mobilenumber=?,email=?,address=?,status=? where appuser_pk=?");
                pstmt.setString(1, name);
                pstmt.setString(2, mobilenumber);
                pstmt.setString(3, email);
                pstmt.setString(4, address);
                pstmt.setString(5, status);
                pstmt.setInt(6, appuser_pk);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "User Updated  successfully");
                setVisible(false);
                new ManageUser().setVisible(true);
            } catch (SQLException e) {
                e.printStackTrace();  // Log the SQL exception for debugging purposes
                JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
            } catch (Exception e) {
                e.printStackTrace();  // Log any other exceptions for debugging purposes
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

