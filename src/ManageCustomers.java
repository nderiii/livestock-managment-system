    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String worker = txtWorker.getText();
        String livestock = txtLivestock.getText();
        String time = txtTime.getText();
        String location = txtLocation.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection Con = ConnectionProvider.getCon();
                PreparedStatement ps = Con.prepareStatement("Insert into customer(worker,livestock,time,location)values(?,?,?,?)");
                ps.setString(1, worker);
                ps.setString(2, livestock);
                ps.setString(3, time);
                ps.setString(4, location);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Workers added successfully");
                setVisible(false);
                new ManageCustomers().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        // TODO add your handling code here:
         //set data to their textfield
        DefaultTableModel tblModel = (DefaultTableModel)tableCustomer.getModel();
        //set data to text field when raw is selected
        String name = tblModel.getValueAt(tableCustomer.getSelectedRow(), 1).toString();
        String livestock = tblModel.getValueAt(tableCustomer.getSelectedRow(), 2).toString();
        String time = tblModel.getValueAt(tableCustomer.getSelectedRow(), 3).toString();
        String location = tblModel.getValueAt(tableCustomer.getSelectedRow(), 4).toString();
        txtWorker.setText(name);
        txtLivestock.setText(livestock);
        txtTime.setText(time);
        txtLocation.setText(location);
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCustomerMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String worker = txtWorker.getText();
        String livestock = txtLivestock.getText();
        String time = txtTime.getText();
        String location = txtLocation.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection Con = ConnectionProvider.getCon();
                PreparedStatement ps = Con.prepareStatement("Update Customer set worker=?,livestock=?,time=?,location=? where customer_pk");
                ps.setString(1, worker);
                ps.setString(2, livestock);
                ps.setString(3, time);
                ps.setString(4, location);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer Updates successfully");
                setVisible(false);
                new ManageCustomers().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tableCustomer.getModel();

        //delete row
        if (tableCustomer.getSelectedRowCount() == 1) {
            tblmodel.removeRow(tableCustomer.getSelectedRow());
            // if single row is selected then delete
        } else {
            if (tableCustomer.getRowCount() == 0)
            //if table is empty (no data)then display message
            {
                JOptionPane.showMessageDialog(this, "Table is empty");
            }else
            {
                 //if table row is not empty but row is not selected or multiple row is selected
                JOptionPane.showMessageDialog(this, "Please select single row for delete");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

