    private void tableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoryMouseClicked
        // TODO add your handling code here:
        //set data to their textfield
        DefaultTableModel tblModel = (DefaultTableModel)tableCategory.getModel();
        //set data to text field when raw is selected
        String name = tblModel.getValueAt(tableCategory.getSelectedRow(), 1).toString();
         String quantity = tblModel.getValueAt(tableCategory.getSelectedRow(), 2).toString();
        txtName.setText(name);
        txtQuantity.setText(quantity);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCategoryMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel=(DefaultTableModel) tableCategory.getModel();
        String name = txtName.getText();
        String quantity = txtQuantity.getText();
        if(validateFields())
        {     
            JOptionPane.showMessageDialog(null, "All fields are required");
        }
        else
        {
            try
            {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update livestock set name=?,quantity=? where livestock_pk=?");
                ps.setString(1,name);
                ps.setString(2,quantity);
                ps.setInt(3,livestock_pk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Livestock Updated sucessfully");
                setVisible(false);
                new ManageLivestock().setVisible(true);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tableCategory.getModel();

        //delete row
        if (tableCategory.getSelectedRowCount() == 1) {
            tblmodel.removeRow(tableCategory.getSelectedRow());
            // if single row is selected then delete
        } else {
            if (tableCategory.getRowCount() == 0)
            //if table is empty (no data)then display message
            {
                JOptionPane.showMessageDialog(this, "Table is empty");
            }else
            {
                 //if table row is not empty but row is not selected or multiple row is selected
                JOptionPane.showMessageDialog(this, "Please select single row for delete");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

